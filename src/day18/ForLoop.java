package day18;

public class ForLoop {
    public static void main(String[] args) {


        for (int x=1;x<=5;++x  ){
            System.out.println("x=" + x);
            System.out.println("Happy Birthday");
        }

        for(int counter =10; counter>0;--counter){
            System.out.println(counter);
        }




    }
}
