public class WhilLoopIntro {
    public static void main(String[] args) {
        int count=1;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");

        System.out.println("Count is "+ count);
        int cnt=1;
        while(cnt<=5){
            System.out.println("Hello");
            System.out.println("cnt value is "+cnt);
            cnt++;
        }


    }
}
